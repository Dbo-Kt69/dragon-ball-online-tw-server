bool CFixedStringListNode::DoIt( bool (*pfnToDo)( void * pvData ) )
{
	Assert( IsValid() == true );

	return pfnToDo( this );
}


//-----------------------------------------------------------------------------
// Name			: IsValid
// Desc			: 
// Privilege	: public
// Parameter	: 
// Return		: 
//-----------------------------------------------------------------------------

bool CFixedStringListNode::IsValid( void ) const
{
	if( m_pcPrev != NULL &&
		m_pcPrev->GetNext() != this )
	{
		return false;
	}

	if( m_pcNext != NULL &&
		m_pcNext->GetPrev() != this )
	{
		return false;
	}

	return true;
}


