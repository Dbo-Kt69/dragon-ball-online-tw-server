LRESULT CApiWinApp::OnDestroy( void )
{
	CNavigator::DestroyInstance();

	if( m_pcRenderer != NULL )
	{
		delete m_pcRenderer;
		m_pcRenderer = NULL;
	}

	return 0L;
}


